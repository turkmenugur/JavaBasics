package StringClass.CharacterMethods;

public class CharAtMethod {
    /* CharAt metodu ile String içerisindeki karaktere ulaşmak */

    /* Bir String nesnesi içerisisinden belli bir noktadaki tek bir karakteri seçmek istediğimizdee bu metodu kullanırız. */

    public static void main(String[] args) {
        String text = "Java";
        System.out.println("üçüncü harf: " + text.charAt(2));
    }
}
